CREATE TABLE IF NOT EXISTS user_inventory (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT UNIQUE NOT NULL,
  madera INT NOT NULL DEFAULT 0,
  hierbas INT NOT NULL DEFAULT 0,
  metal INT NOT NULL DEFAULT 0,
  semillas INT NOT NULL DEFAULT 0,
  notas_de_campo INT NOT NULL DEFAULT 0,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS bird_cards (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL,
  bird_id BIGINT NOT NULL, -- Referencia al ID del catalogo JSON
  nombre VARCHAR(255) NOT NULL,
  vida INT NOT NULL,
  dano_ataque INT NOT NULL,
  defensa INT NOT NULL,
  tipo VARCHAR(50) NOT NULL,
  velocidad INT NOT NULL,
  imagen_url VARCHAR(500),
  fecha_obtencion TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
